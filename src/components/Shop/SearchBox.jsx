// Импорт React
import React from "react";
// Импорт стилей из модуля Shop
import s from "./Shop.module.scss";

// Компонент поисковой строки с чувствительностью к регистру
// Принимает пропсы:
// - value: текущее значение поискового запроса
// - onChange: функция обратного вызова при изменении значения поиска
export default function SearchBox({ value, onChange }) {
  // Возвращаем JSX разметку компонента поисковой строки
  return (
    // Контейнер элемента управления (поиска)
    <div className={s.control}>
      {/* Метка для поля поиска с пояснением о чувствительности к регистру */}
      <label className={s.label}>Поиск (чувствителен к регистру)</label>
      
      {/* Поле ввода для поискового запроса */}
      <input
        className={s.input} // Применение стилей input из SCSS-модуля
        type="text" // Тип поля - текстовый input
        value={value} // Текущее значение поискового запроса (контролируемый компонент)
        placeholder="Например, Бананы (а не banana)" // Подсказка в пустом поле с примером
        // Обработчик изменения значения: вызываем onChange с новым значением
        onChange={(e) => onChange(e.target.value)}
      />
    </div>
  );
}





