// Импорт React
import React from "react";
// Импорт стилей из модуля Shop
import s from "./Shop.module.scss";

// Компонент таблицы товаров для отображения в виде таблицы
// Принимает пропс items: массив товаров для отображения
export default function ProductsTable({ items }) {
  // Если массив товаров пустой, возвращаем сообщение "Нет товаров"
  if (items.length === 0) return <div className={s.empty}>Нет товаров</div>;
  
  // Возвращаем JSX разметку компонента таблицы товаров
  return (
    // Обертка таблицы с применением стилей tableWrap
    <div className={s.tableWrap}>
      {/* Таблица товаров с применением стилей table */}
      <table className={s.table}>
        {/* Заголовок таблицы (thead) */}
        <thead>
          {/* Строка заголовков (tr) */}
          <tr>
            {/* Столбец "Название" */}
            <th>Название</th>
            
            {/* Столбец "Категория" */}
            <th>Категория</th>
            
            {/* Столбец "Цена, тг." с выравниванием по правому краю */}
            <th style={{textAlign:'right'}}>Цена, тг.</th>
          </tr>
        </thead>
        
        {/* Тело таблицы (tbody) с данными товаров */}
        <tbody>
          {/* Маппинг массива товаров: для каждого товара создаем строку таблицы */}
          {items.map(p => (
            // Строка таблицы с уникальным ключом (id товара)
            <tr key={p.id}>
              {/* Ячейка с названием товара */}
              <td>{p.name}</td>
              
              {/* Ячейка с категорией товара в виде чипса/метки */}
              <td>
                <span className={s.catChip}>{p.category}</span>
              </td>
              
              {/* Ячейка с ценой товара, выровненная по правому краю с жирным шрифтом */}
              <td style={{textAlign:'right', fontWeight:700}}>{p.price}</td>
            </tr>
          ))}
        </tbody>
      </table>
    </div>
  );
}




